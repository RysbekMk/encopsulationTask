public class Cat {
    private String name;
    private String play;
    private int age;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }

    public void setPlay(String play) {
        this.play = play;
    }

    public String getPlay() {
        return play;
    }
}
