public class Fish {
    private String tofloat;
    private int mass;
    private String colors;

    public void setTofloat(String tofloat) {
        this.tofloat = tofloat;
    }

    public String getTofloat() {
        return tofloat;
    }

    public void setMass(int mass) {
        this.mass = mass;
    }

    public int getMass() {
        return mass;
    }

    public void setColors(String colors) {
        this.colors = colors;
    }

    public String getColors() {
        return colors;
    }
}
