public class Parrot {
    private String speak;
    private String colors;
    private int mass;

    public void setSpeak(String speak) {
        this.speak = speak;
    }

    public String getSpeak() {
        return speak;
    }

    public void setColors(String colors) {
        this.colors = colors;
    }

    public String getColors() {
        return colors;
    }

    public void setMass(int mass) {
        this.mass = mass;
    }

    public int getMass() {
        return mass;
    }
}
