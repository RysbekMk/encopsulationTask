public class Dog {
    private String name;
    private int age;
    private String colors;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }

    public void setColors(String colors) {
        this.colors = colors;
    }

    public String getColors() {
        return colors;
    }
}
